def listar_fontes() -> list[FonteRegistry]:
    return list(_FONTES)


def fontes_implementadas() -> list[FonteRegistry]:
    return [fonte for fonte in _FONTES if fonte.status_suporte == "suportado"]


def obter_fonte(fonte: str) -> FonteRegistry | None:
    for item in _FONTES:
        if item.fonte == fonte:
            return item
    return None


def listar_datasets(fonte: str) -> list[DatasetFonte]:
    fonte_item = obter_fonte(fonte)
    return [] if fonte_item is None else list(fonte_item.datasets)


def dataset_por_row_kind(row_kind: str) -> DatasetFonte | None:
    for fonte in _FONTES:
        for dataset in fonte.datasets:
            if dataset.row_kind == row_kind:
                return dataset
    return None


def dataset_por_member_name(fonte: str, member_name: str, ano: int) -> DatasetFonte | None:
    fonte_item = obter_fonte(fonte)
    if fonte_item is None:
        return None
    for dataset in fonte_item.datasets:
        if dataset.render_member_name(ano=ano) == member_name:
            return dataset
    return None
