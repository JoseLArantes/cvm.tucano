from __future__ import annotations

from dataclasses import dataclass
from typing import Literal

from app.services.financeiro_mapas import arquivos_demonstracao

FonteChave = Literal["cadastro", "dfp", "itr", "fre", "fca", "ipe", "cgvn", "vlmo"]
TipoDistribuicao = Literal["csv_unico", "zip_anual"]
StatusSuporte = Literal["suportado", "pendente_mapeamento"]


@dataclass(frozen=True)
class DatasetFonte:
    dataset: str
    descricao: str
    member_name_template: str
    row_kind: str | None
    destino_promovido: str | None
    obrigatorio: bool
    status_suporte: StatusSuporte
    normalizador: str | None
    chaves_relacao: tuple[str, ...]
    observacoes: str | None = None

    def render_member_name(self, *, ano: int | None) -> str:
        if "{ano}" in self.member_name_template:
            if ano is None:
                raise ValueError("ano_obrigatorio_para_member_template")
            return self.member_name_template.format(ano=ano)
        return self.member_name_template


@dataclass(frozen=True)
class FonteRegistry:
    fonte: FonteChave
    familia: str
    descricao: str
    tipo_distribuicao: TipoDistribuicao
    dataset_path_template: str
    arquivo_principal_template: str
    primeiro_ano: int | None
    ultimo_ano: int | None
    obrigatorio: bool
    status_suporte: StatusSuporte
    dependencias: tuple[FonteChave, ...]
    datasets: tuple[DatasetFonte, ...]

    @property
    def total_datasets(self) -> int:
        return len(self.datasets)

    @property
    def datasets_obrigatorios(self) -> int:
        return sum(1 for item in self.datasets if item.obrigatorio)

    @property
    def datasets_opcionais(self) -> int:
        return sum(1 for item in self.datasets if not item.obrigatorio)

    def render_arquivo_principal(self, *, ano: int | None) -> str:
        if "{ano}" in self.arquivo_principal_template:
            if ano is None:
                raise ValueError("ano_obrigatorio_para_arquivo_principal")
            return self.arquivo_principal_template.format(ano=ano)
        return self.arquivo_principal_template


